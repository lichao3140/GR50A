package com.runvision.bean;

public class Date {
	private int Year;
	private int Month;
	private int Date;
	private int Hours;
	private int Minutes;
	private int second;
	
	
	public void Date()
	{
		this.Year=Year;
		this.Month=Month;
		this.Date=Date;
		this.Hours=Hours;
		this.Minutes=Minutes;
		this.second=second;
	}
	public int getYear() {
		return Year;
	}
	public void setYear(int year) {
		Year = year;
	}
	public int getMonth() {
		return Month;
	}
	public void setMonth(int month) {
		Month = month;
	}
	public int getDate() {
		return Date;
	}
	public void setDate(int date) {
		Date = date;
	}
	public int getHours() {
		return Hours;
	}
	public void setHours(int hours) {
		Hours = hours;
	}
	public int getMinutes() {
		return Minutes;
	}
	public void setMinutes(int minutes) {
		Minutes = minutes;
	}
	public int getSecond() {
		return second;
	}
	public void setSecond(int second) {
		this.second = second;
	}
	
	
	
}
